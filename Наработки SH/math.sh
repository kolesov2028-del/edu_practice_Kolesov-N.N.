#!/bin/bash

read -p "Введите первое число: " a
read -p "Введите второе число: " b

if [ -z "$a" ] || [ -z "$b" ]; then
    echo "Ошибка: нужно ввести два числа!"
    read -p "Нажмите Enter, чтобы закрыть окно..."
    exit 1
fi

echo "Сумма: $((a + b))"
echo "Разность: $((a - b))"
echo "Произведение: $((a * b))"

read -p "Нажмите Enter, чтобы закрыть окно..."